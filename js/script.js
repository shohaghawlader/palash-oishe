const revealObserver = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('visible');
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.12 });
document.querySelectorAll('.reveal').forEach(el => revealObserver.observe(el));

function updateDaysSince() {
  const weddingDate = new Date('2025-12-15T00:00:00');
  const now = new Date();
  const diff = now - weddingDate;
  const days = Math.max(0, Math.floor(diff / (1000 * 60 * 60 * 24)));
  document.getElementById('daysSince').textContent = days.toString();
}
updateDaysSince();

const galleryGrid = document.getElementById('galleryGrid');
const filterButtons = document.querySelectorAll('.filter-btn');
const lightbox = document.getElementById('lightbox');
const lightboxImage = document.getElementById('lightboxImage');
const lightboxCaption = document.getElementById('lightboxCaption');
const lightboxClose = document.getElementById('lightboxClose');

function renderGallery(filter = 'all') {
  galleryGrid.innerHTML = '';
  const items = filter === 'all' ? galleryItems : galleryItems.filter(item => item.category === filter);
  items.forEach(item => {
    const card = document.createElement('figure');
    card.className = 'gallery-item';
    card.dataset.category = item.category;
    card.innerHTML = `
      <img src="${item.src}" alt="${item.title}" loading="lazy">
      <figcaption class="gallery-caption">${item.title}</figcaption>
    `;
    card.addEventListener('click', () => openLightbox(item));
    galleryGrid.appendChild(card);
  });
}
function openLightbox(item) {
  lightboxImage.src = item.src;
  lightboxCaption.textContent = item.title;
  lightbox.classList.add('open');
  lightbox.setAttribute('aria-hidden', 'false');
  document.body.classList.add('no-scroll');
}
function closeLightbox() {
  lightbox.classList.remove('open');
  lightbox.setAttribute('aria-hidden', 'true');
  lightboxImage.src = '';
  document.body.classList.remove('no-scroll');
}
lightboxClose.addEventListener('click', closeLightbox);
lightbox.addEventListener('click', (event) => { if (event.target === lightbox) closeLightbox(); });
document.addEventListener('keydown', (event) => { if (event.key === 'Escape') closeLightbox(); });

filterButtons.forEach(btn => {
  btn.addEventListener('click', () => {
    filterButtons.forEach(b => b.classList.remove('active'));
    btn.classList.add('active');
    renderGallery(btn.dataset.filter);
  });
});
document.querySelectorAll('[data-filter-link]').forEach(link => {
  link.addEventListener('click', () => {
    const filter = link.dataset.filterLink;
    const btn = document.querySelector(`.filter-btn[data-filter="${filter}"]`);
    if (btn) setTimeout(() => btn.click(), 350);
  });
});
renderGallery();

