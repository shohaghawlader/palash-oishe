const wishForm = document.getElementById('wishForm');
const wishList = document.getElementById('wishList');
const defaultWishes = [
  { name: 'Family Blessing', relation: 'Loved Ones', message: 'May your married life be filled with love, patience, understanding, and endless togetherness.' },
  { name: 'A Sacred Wish', relation: 'Wedding Memory', message: 'May this union always remain blessed with respect, joy, and divine protection.' }
];
function getWishes() {
  const saved = localStorage.getItem('palashOisheWishes');
  return saved ? JSON.parse(saved) : defaultWishes;
}
function saveWishes(wishes) {
  localStorage.setItem('palashOisheWishes', JSON.stringify(wishes));
}
function renderWishes() {
  const wishes = getWishes();
  wishList.innerHTML = wishes.map(wish => `
    <article class="wish-item">
      <strong>${wish.name}</strong>
      <small>${wish.relation || 'Guest'}</small>
      <p>${wish.message}</p>
    </article>
  `).join('');
}
wishForm.addEventListener('submit', (event) => {
  event.preventDefault();
  const wishes = getWishes();
  wishes.unshift({
    name: document.getElementById('wishName').value.trim(),
    relation: document.getElementById('wishRelation').value.trim(),
    message: document.getElementById('wishMessage').value.trim()
  });
  saveWishes(wishes);
  wishForm.reset();
  renderWishes();
});
renderWishes();
